<p>The first step to accessibility in marketing is making your text inclusive. If no one can read what you are writing, it is as if you haven’t written anything of value at all. There are a number of easy ways you can make your text accessible.</p>

<ol>
  <li>
    <p>Inclusive Font</p>
    <ol type="a">
      <li>
        <p>Decorative and handwritten fonts may give a sense of elegance, however, they are relatively difficult to read, even for those with superior eyesight. Opt for a sans-serif font family such as Arial, Calibri, or Verdana.</p>
      </li>
      <li>
        <p>Please note that Verdana tends to be a favourite in accessibility as it was designed in a way that avoids letter confusion, namely the differentiation of the capital letter I and the lowercase letter l. In Arial, and other fonts, capital I and the lowercase l are indistinguishable.</p>
      </li>
      <li>
        <p>Illusion vs <span style="font-family: arial, sans-serif">Illusion</span>: When writing text, please keep in mind to use mixed casing, use uppercase letters at the beginning of sentences, proper names, places etc. Avoid using all uppercase letters or all lowercase letters as word differentiation is more difficult.</p>
        <p>Examples:</p>
        <p>All uppercase: OUR STARTUP LIKES TO HOLD MEETINGS ON MONDAY NIGHTS.</p>
        <p>Sans-serif:  Our startup likes to hold meetings on Monday nights.</p>
        <p>Serif: <span style="font-family: serif">Our startup likes to hold meetings on Monday nights.</span></p>
        <p>Decorative font: <span style="font-family: cursive;">Our startup likes to hold meetings on Monday nights.</span></p>
        <p>Now here is the same text is slightly blurred*:</p>
        <img src="" alt="blurry text image">
        <p>Which is the easiest to read?</p>
      </li>
    </ol>
  </li>
  <li>
    <p>Contrast</p>
    <ol type="a">
      <li>
        <p>The more contrast between the background and text, the easier it is to read the content. There should be high contrast between text and background.</p>
      </li>
      <li>
        <p>There are tools available to designers, marketers, and everyone else in fact that allow you to test the contrast between your text and background.</p>
      </li>
      <li>
        <p>Using this tool you should look to have a contrast ratio higher than <b>4.6:1</b>, with the maximum contrast ratio being <b>21:1</b>.</p>
        <p>Examples:</p>
        <p>Highest contrast ratio of <b>21:1</b>: <span style="color: #000000;">This text is more easy to read because it is highly contrasted.</span></p>
        <p>Low contrast ratio of <b>1.84:1</b>: <span style="color: #BFBFBF;">This text is more difficult to read because it has a low contrast.</span></p>
        <p>Resource: <a href="https://webaim.org/resources/contrastchecker/" target="_blank">Learn more about contrast</a>.</p>
      </li>
    </ol>
  </li>
  <li>
    <p>Colour</p>
    <ol type="a">
      <li>
        <p>Colour relates heavily to contrast, and while it may add personality it should be used sparingly. Materials, both printed and online, are easier to read when they are black and white. A rule of thumb is to restrict use of colours to headings, titles, and emphasized materials.</p>
        <p>Examples:</p>
        <p>Coloured example: <span style="color: cyan;">Te</span><span style="color: purple;">xt is</span><span style="color: red;"> easiest to rea</span><span style="color: purple;">d when black</span><span style="color: blue;"> is on a</span><span style="color: green;"> white</span><span style="color: orange;"> background.</span></p>
        <p>Black and white example: Text is easiest to read when black is on a white background.</p>
      </li>
    </ol>
  </li>
  <li>
    <p>Text Size</p>
    <ol type="a">
      <li>
        <p>Text is easiest to read when it is sized between 12 and 18 pts. Anything outside of that range is difficult to read, as it either decreases the visibility of the words or takes up too much space to easily understand the concept.</p>
      </li>
      <li>
        <p>Also take into consideration that fonts differ, choose a size that is appropriate for the font family.</p>
        <p>Examples:</p>
        <p>Small text size: <span style="font-size: 0.5em;">Choosing the right sized text is important.</span></p>
        <p>Optimal text size:  Choosing the right sized text is important.</p>
        <p>Now here is the same text slightly blurred:</p>
        <img src="" alt="blurry image of text">
      </li>
    </ol>
  </li>
  <li>
    <p>Emphasis and Bolding</p>
    <ol type="a">
      <li>
        <p>For the purposes of emphasis choose to bold the text, rather than italicize it. Italics are more difficult to read as they shift people’s preconceived understanding of where letters ought to be and what they should look like. Italics also tends to thin the font lowering the readability.</p>
      </li>
      <li>
        <p>Avoid using fonts that are too thin or light. Thin fonts, much like low contrast text, make it difficult to separate the background from the text.</p>
        <p>Examples:</p>
        <p>Italicized Example: For the purposes of <em>emphasis</em> choose to bold the text, rather than italicize it.</p>
        <link href="https://fonts.googleapis.com/css?family=Lato:100" rel="stylesheet">
        <p>Thin Font Example: <span style="font-family: 'Lato', sans-serif;">For the purposes of emphasis choose to bold the text, rather than italicize it.</span></p>
        <p>Bolded Example: For the purposes of <b>emphasis</b> choose to bold the text, rather than italicize it.</p>
      </li>
    </ol>
  </li>
  <li>
    <p>Leading</p>
    <ol type="a">
      <li>
        <p>Leading is the space between two or more lines of text. In order to increase the readability of a paragraph, or other text groupings, the leading should be around 30% of the text size. This will help readers to move from one line to the next more easily.</p>
      </li>
      <li>
        <p>If the text is heavier or more bold, the leading should be increased to improve comprehension and readability.</p>
      </li>
    </ol>
  </li>
  <li>
    <p>Letter Spacing</p>
    <ol type="a">
      <li>
        <p>Be sure to leave space between letters and words. Selecting a font family with fixed-width can help with word spacing. Avoid overcrowding your text as words and letters will be difficult to identify.</p>
        <p>Examples:</p>
        <p>Fixed-width font: <span style="font-family: monospace;">All of the words and letters are clearly separated.</span></p>
        <p>Proportional font: All of the words and letters are clearly separated.</p>
        <p>Overcrowded spacing: <span style="letter-spacing: -1.5px;">All of the words and letters are clearly separated.</span></p>
      </li>
    </ol>
  </li>
  <li>
    <p>Dividing Text into Columns</p>
    <ol type="a">
      <li>
        <p>If you are writing longer articles dividing your text into columns increases comprehension as it requires less eye movement. Readers do not have to use their peripheral vision as often if text is broken into columns.</p>
      </li>
    </ol>
  </li>
</ol>

<p>*Blurred examples are used to increase comprehension of using the correct font sizes and families. Blurred examples do not represent every use case and are not used to assume that people with vision loss have the same experiences.</p>
