<p>Unlike photographs, videos are a combination of both visual and auditory cues, which means you are intended to both see and hear a video to get maximum impact. Not everyone has the privilege of being able to see, hear, and understand video content. Videos are a powerful medium that should be created with care to ensure that everyone is able to understand what is going on. It is important that if using video to make all key features both audible and visual.</p>

<p>Here are two (2) important items to consider when making or sharing a video, and how to implement them:</p>

<ol>
  <li>
    <p>Captions</p>
    <p>Captions are important in numerous settings and circumstances. Captions increase the user’s ability to understand your content and provide an alternative for those who are not able to hear it. This can happen for a number of reasons including, but not limited to: poor audio quality in video, hearing loss, being in a noisy public space, or muted computer or phone speakers.</p>
    <p>There are two (2) types of captions, closed or open. Closed captions are the most common caption type, as they can be turned on and off. Open captions, on the other hard, are always on, and usually placed directly into the video itself.</p>
    <p>Captions are a useful tool that can get you more attention and improve your company’s reach. There are three (3) rules your captions should follow:</p>
    <ol>
      <li>
        <p>Captions should provide <b>exact or equivalent content</b> to what is said in the video audi</p>
      </li>
      <li>
        <p>Captions should appear <b>in sync</b>, or as close as possible to when they are being said in the video.</p>
      </li>
      <li>
        <p>Captions should be <b>available and easy to access</b> for those that would like to use it.</p>
      </li>
    </ol>
    <p>The easiest way to include closed captions in your videos is to have your video hosted on YouTube. There are numerous ways you can upload and synchronize captions.</p>
    <p>Here is a <a href="ncdae.org/resources/cheatsheets/youtube.php" target="_blank">link on how to add closed captions and transcripts to YouTube<i class="fas fa-external-link-alt"></i></a>.</p>
    <p>If you are not posting your video on YouTube, you may want to include open captions directly on your video. This is the easiest way to increase your reach, and not spend extra money on captioning software. Here are some steps on how to input open captions on your video without being specific:</p>
    <ol>
      <li>
        <p>Start with a finished video. This means that all the editing has been done and the clips are in the correct order.</p>
      </li>
      <li>
        <p>Write out all of the lines (spoken words) in the video.</p>
      </li>
      <li>
        <p><b>Make notes</b> on when specific lines are said in a video. Write down the times when they start and finish.</p>
      </li>
      <li>
        <p><b>Add subtitles or text cards</b> to the bottom third of your video. </p>
      </li>
      <li>
        <p>Add the lines from the video to the subtitles/ text cards, starting each line at the recorded start time.</p>
      </li>
      <li>
        <p>Ensure that the user has enough time to read the text before it leaves the screen.</p>
      </li>
      <li>
        <p><b>Publish</b> your video with subtitles.</p>
        <p>Reminder: If you publish your video with open captions they will be a part of the video and cannot be closed.</p>
      </li>
    </ol>
  </li>
  <li>
    <p>Described Video / Audio Descriptions</p>
    <p>Described video (DV) and audio descriptions both refer to providing audio alternatives to what is being shown on screen. DV is targeted at individuals with vision loss in order to provide a description of what is happening in a video for the purposes of increasing understanding and comprehension. DV is usually used as an accessibility afterthought. This means people add DV once the video has been made and they realize it is not accessible.</p>
    <p>If creating a video, and you do not want to include DV, make sure all the key information in a video is said verbally while filming or during narration. For example, there is a television show where people want to find out the price of their antiques. It would be useful for the antiques appraiser to describe the piece, perhaps stating, “this chair appears to be from the 18th century, the velvet cushions seem a bit worn, there are some fine scratches on the side”, rather than saying, “yes, it is a nice chair”. An alternative for this example would be for the show’s host or narrator to describe the antique.</p>
    <p class="font-bold">Tip:</p>
    <p>Think about including a transcript of the video. Transcripts are more in-depth than captions as they also describe what is going on in the video, not simply what is being said. Transcripts can also be <a href="http://ncdae.org/resources/cheatsheets/youtube.php" target="_blank">added to YouTube videos<i class="fas fa-external-link-alt"></i></a>. Transcripts are occasionally favoured over audio as those using screen readers are used to processing the information received through their screen reader at a much faster rate than that presented in the video.</p>
  </li>
</ol>
