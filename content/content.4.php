<p>Depending on how your startup is structured, the marketing and graphic design team may be required to implement alt-text on images. If your startup uses a web creation platform such as Shopify, Weebly, WordPress, or any of the large number of similar services this would be within your wheelhouse. If your team builds their webpage from scratch this duty would fall to your developers.</p>

<p>Alt-text refers to alternate text for images, frequently used by individuals living with sight loss that use screen readers or alternate assistive technology for web browsing and computer or mobile-related work. By providing alt-text for images you are allowing someone to understand what is happening on a page, whether or not they are able to see it. Alt-text also improves your websiteâ€™s SEO and brings more organic traffic to your site, company, and products.</p>

<p>Depending on which website builder your team is using there are various locations and instructions on how to implement the alt-text. This usually involves clicking on an image or page and locating the alt-text button or form.</p>

